def current_lang() -> str:
    """Return the active language, defaulting to English.

    Reads ``st.session_state["lang"]`` when Streamlit is available; falls back
    to ``_DEFAULT_LANG`` so `t()` is usable from tests and code-gen paths that
    never import Streamlit.
    """
    try:
        import streamlit as st

        return st.session_state.get("lang", _DEFAULT_LANG)
    except Exception:
        return _DEFAULT_LANG


def t(key: str, **fmt: object) -> str:
    """Look up ``key`` in the active language dict; fall back to English then
    to the key itself. ``**fmt`` is forwarded to ``str.format`` on the value."""
    lang = current_lang()
    value = STRINGS.get(lang, {}).get(key)
    if value is None:
        value = STRINGS[_DEFAULT_LANG].get(key, key)
    if fmt:
        try:
            return value.format(**fmt)
        except (KeyError, IndexError):
            return value
    return value
